package com.ebay.cloud.cms.sysmgmt.server;

public class RequestIdSequence {
    private ThreadLocal<Integer> seq = new ThreadLocal<Integer>();
    
    
    public String getNext() {
        Integer id = (Integer)seq.get();
        if (id == null) {
            id = 1001;
        }
        
        String result = String.format("%d-%d", Thread.currentThread().getId(), id);
        id++;
        seq.set(id);
        return result;
    }


}
