package com.ebay.cloud.cms.metadata.mongo.converter;

import java.io.IOException;

import org.codehaus.jackson.JsonNode;
import org.codehaus.jackson.JsonParser;
import org.codehaus.jackson.JsonProcessingException;
import org.codehaus.jackson.map.DeserializationContext;
import org.codehaus.jackson.map.JsonDeserializer;

public class MongoObjectIdConverter {
    //private static ObjectMapper mapper = new ObjectMapper();
    
    public static class ObjectIdDeserializer extends JsonDeserializer<String> {
        
        public String deserialize(JsonParser jp,
                DeserializationContext ctxt) throws IOException,
                JsonProcessingException {
            JsonNode n = jp.readValueAsTree();
            JsonNode value = n.get("$oid");
            if (value == null) {
                return n.getTextValue();
            }
            else {
                return value.getTextValue();
            }
        }
    }
}
